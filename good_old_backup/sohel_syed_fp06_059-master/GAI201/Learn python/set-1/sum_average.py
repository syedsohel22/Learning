# 4. **Sum and Average**: Write a Python program that calculates and prints the sum and average of a list of numbers.
#     - *Input*: [10, 20, 30, 40]
#     - *Output*: "Sum: 100, Average: 25.0"

# Input list of numbers
numbers = [10, 20, 30, 40]

# Calculate the sum of the numbers using the built-in sum() function
sum_of_numbers = sum(numbers)

# Calculate the average of the numbers
average_of_numbers = sum_of_numbers / len(numbers)

# Printing the sum and average
print("Sum:", sum_of_numbers, ", Average:", average_of_numbers)
