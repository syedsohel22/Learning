# 2. **Data Type Play**: Create variables of each data type (integer, float, string, boolean, list, tuple, dictionary, set) and print their types and values.
#     - *Input*: None
#     - *Output*: "Type of variable1: <class 'int'>, value: 10...""

# Integer
age = 25

# Float
pi = 3.14

# String
name = "John"

# Boolean
is_student = True

# List
fruits = ["apple", "banana", "cherry"]

# Tuple
coordinates = (10, 20)

# Set
unique_numbers = {1, 2, 3, 4, 5}

# Dictionary
student = {"name": "John", "age": 25, "is_student": True}

# Printing types and values
print("Type of age:", type(age), ", value:", age)
print("Type of pi:", type(pi), ", value:", pi)
print("Type of name:", type(name), ", value:", name)
print("Type of is_student:", type(is_student), ", value:", is_student)
print("Type of fruits:", type(fruits), ", value:", fruits)
print("Type of coordinates:", type(coordinates), ", value:", coordinates)
print("Type of unique_numbers:", type(
    unique_numbers), ", value:", unique_numbers)
print("Type of student:", type(student), ", value:", student)
